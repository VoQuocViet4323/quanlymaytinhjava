    private void tableQLMTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableQLMTMouseClicked
        int row = tableQLMT.getSelectedRow();

        String mamay = tableQLMT.getValueAt(row, 0).toString();
        String tenmay = tableQLMT.getValueAt(row, 1).toString();
        String hedieuhanh = tableQLMT.getValueAt(row, 2).toString();
        String hangsanxuat = tableQLMT.getValueAt(row, 3).toString();
        tfMaMay.setText(mamay);
        tfTenMay.setText(tenmay);
        tfHDH.setText(hedieuhanh);
        tfHangSX.setText(hangsanxuat);
    }//GEN-LAST:event_tableQLMTMouseClicked

